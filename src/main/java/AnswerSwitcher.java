public class AnswerSwitcher {
    private boolean switcherStatement = false;
    private String answerChatId;

    public boolean getSwitcherStatement() {
        return switcherStatement;
    }

    public void setSwitcherStatement(boolean switcherStatement) {
        this.switcherStatement = switcherStatement;
    }

    public String getAnswerChatId() {
        return answerChatId;
    }

    public void setAnswerChatId(String answerChatId) {
        this.answerChatId = answerChatId;
    }
}
